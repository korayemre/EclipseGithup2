package reCapDemo_classes;

public class Dortıslem {

	public int Topla(int sayi1, int sayi2) {
		return sayi1 + sayi2;
	}

	public double Cikar(int sayi1, int sayi2) {
		return sayi1 - sayi2;
	}

	public int Carp(int sayi1, int sayi2) {
		return sayi1 * sayi2;
	}

	public int Bol(int sayi1, int sayi2) {
		return sayi1 / sayi2;
	}

}
